package com.prarthana.Countrycategory.cat;

/**
 *
 * @author Pavilion G4
 */
public class CountryCategory {
    private int code,area;
    private long population;
    private String name,capital,continent,description;
    private boolean status;

    public CountryCategory() {
    }

    public CountryCategory(int code, int area, long population, String name, String capital, String continent, String description, boolean status) {
        this.code = code;
        this.area = area;
        this.population = population;
        this.name = name;
        this.capital = capital;
        this.continent = continent;
        this.description = description;
        this.status = status;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public int getArea() {
        return area;
    }

    public void setArea(int area) {
        this.area = area;
    }

    public long getPopulation() {
        return population;
    }

    public void setPopulation(long population) {
        this.population = population;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCapital() {
        return capital;
    }

    public void setCapital(String capital) {
        this.capital = capital;
    }

    public String getContinent() {
        return continent;
    }

    public void setContinent(String continent) {
        this.continent = continent;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }
    
    
    
}
